// –û–±—Ä–∞–±–æ—Ç—á–∏–∫ –∫–æ–º–∞–Ω–¥—ã /accounts
async function handleAccountsCommand(chatId, userName) {
  const zenMoneyToken = process.env.ZENMONEY_TOKEN;
  
  if (!zenMoneyToken) {
    bot.sendMessage(chatId, '‚ùå ZenMoney API –Ω–µ –Ω–∞—Å—Ç—Ä–æ–µ–Ω. –ü—Ä–æ–≤–µ—Ä—å—Ç–µ –ø–µ—Ä–µ–º–µ–Ω–Ω—É—é ZENMONEY_TOKEN.');
    return;
  }
  
  try {
    // –û—Ç–ø—Ä–∞–≤–ª—è–µ–º —Å–æ–æ–±—â–µ–Ω–∏–µ –æ –∑–∞–≥—Ä—É–∑–∫–µ
    const loadingMessage = await bot.sendMessage(chatId, 'üîÑ –ó–∞–≥—Ä—É–∂–∞–µ–º —Å—á–µ—Ç–∞ –∏–∑ ZenMoney...');
    
    // –ü–æ–ª—É—á–∞–µ–º –¥–∞–Ω–Ω—ã–µ –∏–∑ ZenMoney API
    const currentTimestamp = Math.floor(Date.now() / 1000);
    
    const response = await axios.post('https://api.zenmoney.ru/v8/diff', {
      currentClientTimestamp: currentTimestamp,
      serverTimestamp: 0
    }, {
      headers: {
        'Authorization': `Bearer ${zenMoneyToken}`,
        'Content-Type': 'application/json',
        'User-Agent': 'PellaZenMoneyBot/1.0'
      },
      timeout: 30000
    });
    
    const data = response.data;
    const accounts = data.account || {};
    
    if (Object.keys(accounts).length === 0) {
      await bot.editMessageText('üì≠ –°—á–µ—Ç–∞ –Ω–µ –Ω–∞–π–¥–µ–Ω—ã', {
        chat_id: chatId,
        message_id: loadingMessage.message_id
      });
      return;
    }
    
    // –§–æ—Ä–º–∏—Ä—É–µ–º —Å–ø–∏—Å–æ–∫ —Å—á–µ—Ç–æ–≤
    let responseText = `üí∞ –í–∞—à–∏ —Å—á–µ—Ç–∞ (${Object.keys(accounts).length}):\n\n`;
    
    Object.values(accounts).forEach((account, index) => {
      const balance = account.balance ? `${account.balance}` : '0';
      const type = getAccountTypeEmoji(account.type);
      responseText += `${index + 1}. ${type} ${account.title}\n`;
      responseText += `   üíµ –ë–∞–ª–∞–Ω—Å: ${balance}\n`;
      responseText += `   üÜî ID: ${account.id}\n\n`;
    });
    
    // –ï—Å–ª–∏ —Å–æ–æ–±—â–µ–Ω–∏–µ —Å–ª–∏—à–∫–æ–º –¥–ª–∏–Ω–Ω–æ–µ, —Ä–∞–∑–±–∏–≤–∞–µ–º –Ω–∞ —á–∞—Å—Ç–∏
    if (responseText.length > 4000) {
      const chunks = splitMessage(responseText, 4000);
      for (let i = 0; i < chunks.length; i++) {
        if (i === 0) {
          await bot.editMessageText(chunks[i], {
            chat_id: chatId,
            message_id: loadingMessage.message_id
          });
        } else {
          await bot.sendMessage(chatId, chunks[i]);
        }
      }
    } else {
      await bot.editMessageText(responseText, {
        chat_id: chatId,
        message_id: loadingMessage.message_id
      });
    }
    
    console.log(`–û—Ç–ø—Ä–∞–≤–ª–µ–Ω —Å–ø–∏—Å–æ–∫ —Å—á–µ—Ç–æ–≤ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—é ${userName} (${Object.keys(accounts).length} —Å—á–µ—Ç–æ–≤)`);
    
  } catch (error) {
    console.error('–û—à–∏–±–∫–∞ –ø—Ä–∏ –ø–æ–ª—É—á–µ–Ω–∏–∏ —Å—á–µ—Ç–æ–≤:', error);
    await bot.editMessageText('‚ùå –û—à–∏–±–∫–∞ –ø—Ä–∏ –ø–æ–ª—É—á–µ–Ω–∏–∏ —Å—á–µ—Ç–æ–≤ –∏–∑ ZenMoney. –ü—Ä–æ–≤–µ—Ä—å—Ç–µ —Ç–æ–∫–µ–Ω –∏ –ø–æ–ø—Ä–æ–±—É–π—Ç–µ –ø–æ–∑–∂–µ.', {
      chat_id: chatId,
      message_id: loadingMessage.message_id
    });
  }
}

// –í—Å–ø–æ–º–æ–≥–∞—Ç–µ–ª—å–Ω—ã–µ —Ñ—É–Ω–∫—Ü–∏–∏
function getAccountTypeEmoji(type) {
  const emojiMap = {
    'checking': 'üè¶',
    'ccard': 'üí≥',
    'debt': 'üìä',
    'cash': 'üíµ',
    'deposit': 'üèõÔ∏è',
    'loan': 'üè¶'
  };
  return emojiMap[type] || 'üí∞';
}

function splitMessage(text, maxLength) {
  const chunks = [];
  let currentChunk = '';
  
  const lines = text.split('\n');
  
  for (const line of lines) {
    if (currentChunk.length + line.length + 1 > maxLength) {
      if (currentChunk) {
        chunks.push(currentChunk);
        currentChunk = line;
      } else {
        chunks.push(line);
      }
    } else {
      currentChunk += (currentChunk ? '\n' : '') + line;
    }
  }
  
  if (currentChunk) {
    chunks.push(currentChunk);
  }
  
  return chunks;
}


